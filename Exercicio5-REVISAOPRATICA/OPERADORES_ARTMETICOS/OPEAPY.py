A = 15
B = 4

soma = A + B
subtracao = A - B
multiplicacao = A * B
divisao = A / B
divisao_int = A // B
modulo = A % B

print(f"A + B = {soma}")
print(f"A - B = {subtracao}")
print(f"A * B = {multiplicacao}")
print(f"A / B (Real) = {divisao:.2f}")
print(f"A // B (Inteiro) = {divisao_int}")
print(f"A % B = {modulo}")