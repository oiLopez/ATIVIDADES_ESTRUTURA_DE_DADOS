cor = input("Digite o nome de uma cor primária:")
 
if cor == "vermelho":
  print("VERMELHO: calor, energia, perigo")
   
elif cor == "azul":
  print("AZUL: calma, inteligência, frio")
  
elif cor == "amarelo":
  print("AMARELO: alegria, riqueza, luz")
  
else:
  print("Essa cor não faz parte das cores primárias")